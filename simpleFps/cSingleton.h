#define	SINGLETON(x)	cSingleton<x>::GetInstance()

template< class T>
class cSingleton {
public:
	static T& GetInstance()
	{
		static T instance;

		return instance;
	}

protected:
	cSingleton() {}
	virtual ~cSingleton() {}

private:


};